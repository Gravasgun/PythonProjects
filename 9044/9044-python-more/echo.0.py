# 模仿 echo "$@"，展开传入的所有参数，使用while实现

import sys  # 导入 sys 模块，以便使用 sys.argv 获取命令行参数列表

i = 1  # 初始化变量 i，从 1 开始（跳过 sys.argv[0]，即程序名）

# 进入循环，遍历所有命令行参数（从第 1 个参数开始）
while i < len(sys.argv):
    if i > 1:
        # 如果不是第一个参数（即第二个及以后），先打印一个空格作为分隔
        print(" ", end="")  # end="" 表示不要自动换行
    print(sys.argv[i], end="")  # 打印当前参数（不换行）
    i = i + 1  # 移动到下一个参数

print()  # 所有参数打印完后，输出一个换行符，使输出更整洁
